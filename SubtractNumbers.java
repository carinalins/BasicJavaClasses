public class SubtractNumbers{
	public static void main(String [] args) {
		double a = 4.5;
		double b = 7.3;
		
		double c = a - b;
		double d = 98.5 - 3;
		
		System.out.println("Subtract a - b: " + c);
		System.out.println("Subtract other numbers: " + d);
		System.out.println("Subtract 3: " + (42 - 43));
		}
	}