 public class CastingBetweenTypes{

  public static void main(String[] args) {
  	
  
 	float f = 82.1f;
 	double df = (double) f;

 	System.out.println(f);
 	System.out.println(df);


  } //We can 'cast' from one type of number to another 
  // We do this by writing the new type in brackets before the variable
  // Be aware of which way you are converting as you may lose information
}


